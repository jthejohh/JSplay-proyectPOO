    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(!txtUsuario.getText().isEmpty()&&
                !txtNombre.getText().isEmpty()&&
                    !txtDocumento.getText().isEmpty()&&
                        !txtCorreo.getText().isEmpty()&&
                              !txtContraseña.getText().isEmpty()&&
                                    !txtconfirmarContra.getText().equals(txtContraseña.getText())){
            
            Usuario usuario =new Usuario(txtUsuario.getText(),
                                         txtNombre.getText(), txtDocumento.getText(), txtCorreo.getText(), txtContraseña.getText());
            
            if(UsuarioLogic.insertar(usuario)){
            
                JOptionPane.showMessageDialog(this, "Usuario Registrado Exitosamente");
        }
            else{
                JOptionPane.showMessageDialog(this, "este usuario ya exite");
            }
    }//GEN-LAST:event_btnRegistrarActionPerformed
    }
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        if(!txtUsuario.getText().isEmpty()){
            
           
            
            if(UsuarioLogic.eliminar(txtUsuario.getText())){
            
                JOptionPane.showMessageDialog(this, "El usuario se elimino correctamente");
        }
            else{
                JOptionPane.showMessageDialog(this, "no se puede eliminar");
            }
                                                
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    
