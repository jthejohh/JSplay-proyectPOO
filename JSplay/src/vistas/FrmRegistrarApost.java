    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if(!txtUsuario.getText().isEmpty()&&
                !txtNombre.getText().isEmpty()&&
                    !txtDocumento.getText().isEmpty()&&
                        !txtCorreo.getText().isEmpty()&&
                              !txtContraseña.getText().isEmpty()&&
                                    txtconfirmarContra.getText().equals(txtContraseña.getText())){
            
            Usuario usuario =new Usuario(txtUsuario.getText(),
                          txtNombre.getText(), txtDocumento.getText(), txtCorreo.getText(), txtContraseña.getText());
            
            if(UsuarioLogic.modificar(usuario)){
            
                JOptionPane.showMessageDialog(this, "El Usuario se Modifico Exitosamente");
        }
            else{
                JOptionPane.showMessageDialog(this, "Usuario no Encontrado");
            }
    }                                            
        
    }//GEN-LAST:event_btnModificarActionPerformed

